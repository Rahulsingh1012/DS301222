str1 = "Python is a programming language"
print("Original Str : ",str1)

# print(str1[5])

upper_case = str1.upper()
print("Upper : ",upper_case)

lower_case = str1.lower()
print("Lower : ",lower_case)

swap_case = str1.swapcase()
print("Swapcase : ",swap_case)

length = len(str1)
print("Length : ",length)

ends_with = str1.endswith("ge")
print("Endswith : ",ends_with)

starts_with = str1.startswith("p")
print("Startswith : ",starts_with)


